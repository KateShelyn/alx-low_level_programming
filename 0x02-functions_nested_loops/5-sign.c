#include "main.h"
/**
 * print_sign - function
 * Description - using print_sign function
 * @n: input letter
 * this program prints
 * Return: 0
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	else
	{
		_putchar('0');
		return (0);
	}
}
